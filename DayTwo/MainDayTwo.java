    static void solutionOne() {
        String input = getInput();
        String[] reports = input.split("\n");
        int reportsCounter = 0;
        for (String reportStr : reports) {
            String[] reportItems = reportStr.split(" ");
            List<Integer> levels = new ArrayList<>();

            // Convert using a loop
            for (String s : reportItems) {
                levels.add(Integer.parseInt(s));
            }

            String reportSafetyDirection = "";
            for (int i = 0; i < levels.size(); i++) {
                if (i == levels.size() - 1) { // if it is the last one
                    reportsCounter++;
                    break;
                }

                int currentLevel = levels.get(i);
                int nextLevel = levels.get(i + 1);

                if (currentLevel == nextLevel) {
                    break; // Unsafe because no difference
                }

                if (currentLevel < nextLevel) {
                    if (nextLevel - currentLevel > 3) { // Unsafe because to big difference
                        break;
                    }

                    String direction = "up";
                    if (i == 0) {
                        reportSafetyDirection = direction;
                        continue;
                    }

                    if (!reportSafetyDirection.equals(direction)) {
                        break; // Unsafe, changed direction
                    }

                    continue;
                }

                if (currentLevel - nextLevel > 3) { // Unsafe because to big difference
                    break;
                }

                String direction = "down";
                if (i == 0) {
                    reportSafetyDirection = direction;
                    continue;
                }

                if (!reportSafetyDirection.equals(direction)) {
                    break; // Unsafe, changed direction
                }
            }
        }

        System.out.println(reportsCounter);
    }

    static void solutionTwo() {
        String input = getTestInput();
        String[] reports = input.split("\n");

        int reportsCounter = 0;
        for (String reportStr : reports) {
            String[] reportItems = reportStr.split(" ");
            List<Integer> report = new ArrayList<>();

            // Convert using a loop
            for (String s : reportItems) {
                report.add(Integer.parseInt(s));
            }

            List<Integer> result = isValidReport(report);
            if (result.getFirst() == 1) {
                reportsCounter++;
                continue;
            }

            // initialize Problem Dumper
            for (int i = 0; i < report.size(); i++) {
                List<Integer> clonedReport = new ArrayList<>();
                int exclude = result.get(1);
                for (int j = 0; j < report.size(); j++) {
                    if (j == exclude) {
                        continue;
                    }
                    int value = report.get(j);

                    clonedReport.add(value);
                }

                result = isValidReport(clonedReport);
                if (result.getFirst() == 1) {
                    reportsCounter++;
                    break;
                }
            }
        }

        System.out.println(reportsCounter);
    }

    public static List<Integer> isValidReport(List<Integer> report) {
        List<Integer> validationResult = new ArrayList<>();
        String reportSafetyDirection = "";
        for (int i = 0; i < report.size(); i++) {
            if (i == report.size() - 1) { // if it is the last one
                validationResult.add(1);

                return validationResult;
            }

            int currentLevel = report.get(i);
            int nextLevel = report.get(i + 1);

            if (currentLevel == nextLevel) {
                validationResult.add(0);
                validationResult.add(i);

                return validationResult;
            }

            if (currentLevel < nextLevel) {
                if (nextLevel - currentLevel > 3) { // Unsafe because to big difference
                    validationResult.add(0);
                    validationResult.add(i);

                    return validationResult;
                }

                String direction = "up";
                if (i == 0) {
                    reportSafetyDirection = direction;
                    continue;
                }

                if (!reportSafetyDirection.equals(direction)) {
                    validationResult.add(0);
                    validationResult.add(i);

                    return validationResult;
                }

                continue;
            }

            if (currentLevel - nextLevel > 3) { // Unsafe because to big difference
                validationResult.add(0);
                validationResult.add(i);

                return validationResult;
            }

            String direction = "down";
            if (i == 0) {
                reportSafetyDirection = direction;
                continue;
            }

            if (!reportSafetyDirection.equals(direction)) {
                validationResult.add(0);
                validationResult.add(i);

                return validationResult;
            }
        }

        validationResult.add(1);

        return validationResult;
    }

